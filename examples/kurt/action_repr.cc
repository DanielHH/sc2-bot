ACTION ActionRepr::CreatesUnit(UNIT_TYPEID unit) {
    ABILITY_ID ability = Kurt::GetUnitType(unit)->ability_id;
    if (convert_api_our.count(ability) == 0) {
        return ACTION::INVALID;
    } else {
        return convert_api_our.at(ability);
    }
}

